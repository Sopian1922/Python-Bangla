marks = int(input("Enter your marks: "))
if marks > 100:
    print("Out of range.")
elif marks >= 80:
    print("A+")
elif marks >= 75:
    print("A")
elif marks >= 70:
    print("A-")
elif marks >= 65:
    print("B+")
elif marks >= 60:
    print("B")
elif marks >= 55:
    print("B-")
elif marks >= 50:
    print("C+")
elif marks >= 45:
    print("C")
elif marks >= 40:
    print("D")
else:
    print("F")



